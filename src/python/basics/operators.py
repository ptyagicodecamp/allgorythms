#Operators: +, -, *, /

num1 = 10
num2 = 5

print("Number1: " + str(num1) + ", Number2: " + str(num2))

#Addition
mySum = num1 + num2
print("MySum: " + str(mySum))

#Subtraction
mySub = num1 - num2
print("MySub: " + str(mySub))

#Mutliplication
myMulti = num1 * num2
print("myMulti: " + str(myMulti))

#Division
myDiv = num1 / num2
print("myDiv: " + str(myDiv))